import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import * as esbuild from "esbuild";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const entry = path.join(root, "src/features/vtop-scraper/bookmarklet.ts");
const outJs = path.join(root, "public/vtop-bookmarklet.js");
const outTs = path.join(root, "src/features/vtop-scraper/generated/bookmarkletCode.ts");

async function build() {
  const result = await esbuild.build({
    entryPoints: [entry],
    bundle: true,
    minify: true,
    format: "iife",
    platform: "browser",
    target: ["es2020"],
    write: false,
    define: {
      "process.env.NODE_ENV": '"production"',
    },
  });

  const code = result.outputFiles[0]?.text;
  if (!code) {
    throw new Error("Bookmarklet bundle produced no output.");
  }

  await mkdir(path.dirname(outJs), { recursive: true });
  await writeFile(outJs, code, "utf8");

  await mkdir(path.dirname(outTs), { recursive: true });
  await writeFile(
    outTs,
    `/** Generated by scripts/build-bookmarklet.mjs — do not edit manually. */\nexport const BOOKMARKLET_IIFE = ${JSON.stringify(code)};\n`,
    "utf8"
  );

  console.log(`Bookmarklet built → ${path.relative(root, outJs)}`);
}

build().catch((error) => {
  console.error(error);
  process.exit(1);
});
